# 1.% 占位符
name = '张三'
age = 18
print('我叫%s,今年%d岁了' % (name, age))
print('%10d' % 99)  # 10表示的是总宽度
print('%.3f' % 3.1415926)  # .3表示是小数点后三位
print('%10.3f' % 3.1415926)  # 同时表示宽度和精度  总宽度为10，小数点后3位

# 2.{}
print('我叫{0},今年{1}岁了'.format(name, age))
print('{:.3}'.format(3.1415926))  # 表示三位数
print('{:.3f}'.format(3.1415926))  # 表示三位小数
print('{:10.3f}'.format(3.1415926))  # 总宽度为10，精确到三位小数

# 3.f-string
print(f'我叫{name},今年{age}岁了')
