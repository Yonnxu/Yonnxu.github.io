def fun(arg1, arg2):
    print('argl', arg1)
    print('arg2', arg2)
    arg1 = 100
    arg2.append(10)
    print('argl', arg1)
    print('arg2', arg2)


n1 = 11
n2 = [22, 33, 44]
fun(n1, n2)  # 将位置传参, argl, arg2，是函数定义处的形参，nl,n2是函数调用的处的实参，总结，实参名称与形参名称可以不一样

print(n1,n2)
print('-----------------------重要点！---------------------')
'''在函数调用过程中，进行参数的传递
如果是不可变对象，在函数体的修改不会影响实参的值 arg1的修改为100，不会影响nl的值
如果是可变对象，在函数体的的修改会影响到实参的值arg2的修改，append (10)，会影响到n2的值
'''
